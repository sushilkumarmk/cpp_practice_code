#include<iostream>
#include<math.h>

using namespace std;

int main()
{
    int a=10,b=2;
    cout<<"Sine function:"<<sin(a)<<endl;
    cout<<"Cosine function:"<<cos(a)<<endl;
    cout<<"Tangent function:"<<tan(a)<<endl;
    cout<<"Cotangent function:"<<1/tan(a)<<endl;
    cout<<"Power function:"<<pow(a,b)<<endl;
    cout<<"Square root function:"<<sqrt(a)<<endl;
    return 0;
}
